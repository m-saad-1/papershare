const main = async () => {
  await mongoose.connect(process.env.MONGODB_URI, { family: 4 });

  const paperOps = paperUpdates.map((update) => ({
    updateOne: {
      filter: { _id: update._id },
      update: { $set: update },
    },
  }));

  const noteOps = noteUpdates.map((update) => ({
    updateOne: {
      filter: { _id: update._id },
      update: { $set: update },
    },
  }));

  const [paperResult, noteResult] = await Promise.all([
    Paper.bulkWrite(paperOps, { ordered: true }),
    Note.bulkWrite(noteOps, { ordered: true }),
  ]);

  console.log(JSON.stringify({
    papersMatched: paperResult.matchedCount,
    papersModified: paperResult.modifiedCount,
    notesMatched: noteResult.matchedCount,
    notesModified: noteResult.modifiedCount,
  }, null, 2));

  await mongoose.disconnect();
};

main().catch(async (error) => {
  console.error(error);
  try {
    await mongoose.disconnect();
  } catch {
    // ignore disconnect errors during cleanup
  }
  process.exit(1);
});
